public class Operatorassignment {
	public static void main (String[] args){
		int x =10;
		System.out.println(x);
		System.out.println(x +=1);
		System.out.println(x -=1);
		System.out.println(x *=1);
		System.out.println(x /=1);
		System.out.println(x %=1);
		
		System.out.println((x==11)?11:10);
	}
}
